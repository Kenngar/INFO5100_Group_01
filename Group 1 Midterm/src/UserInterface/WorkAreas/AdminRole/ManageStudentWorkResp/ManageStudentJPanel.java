    private void refreshTable() {
        rowProfiles.clear();
        DefaultTableModel model = new DefaultTableModel(new Object[][]{},
                new String[]{"Student Name", "Email", "Phone #", "Hobbies"}) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        for (StudentProfile sp : business.getStudentDirectory().getStudentList()) {
            rowProfiles.add(sp);
            model.addRow(new Object[]{
                sp.getPerson().getName(),
                sp.getPerson().getEmail(),
                sp.getPerson().getPhoneNumber(),
                sp.getHobbies()
            });
        }
        tblStudent.setModel(model);
    }
