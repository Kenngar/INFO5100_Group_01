    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (selected == null) {
            JOptionPane.showMessageDialog(this, "Select a student first.",
                    "Selection Required", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String name = txtName.getText().trim();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Name cannot be empty.",
                    "Validation", JOptionPane.WARNING_MESSAGE);
            return;
        }
        selected.getPerson().setName(name);
        selected.getPerson().setPhoneNumber(txtPhone.getText().trim());
        selected.setHobbies(txtHobbies.getText().trim());
        refreshTable();
        JOptionPane.showMessageDialog(this, "Student updated.");


    }//GEN-LAST:event_btnUpdateActionPerformed

    private void tblStudentMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudentMousePressed
        // TODO add your handling code here:
        int row = tblStudent.getSelectedRow();
        if (row < 0) {
            return;
        }
        selected = rowProfiles.get(row);
        txtName.setText(selected.getPerson().getName());
        txtPhone.setText(selected.getPerson().getPhoneNumber());
        txtHobbies.setText(selected.getHobbies());

    }//GEN-LAST:event_tblStudentMousePressed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (selected == null) {
            JOptionPane.showMessageDialog(this, "Select a student first.",
                    "Selection Required", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this,
                "Delete student \"" + selected.getPerson().getPersonId() + "\"?",
                "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        business.getStudentDirectory().removeStudent(selected);
        selected = null;
        txtName.setText("");
        txtPhone.setText("");
        refreshTable();
        JOptionPane.showMessageDialog(this, "Student deleted.");

    }//GEN-LAST:event_btnDeleteActionPerformed


